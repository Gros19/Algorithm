package codeUp;

import java.util.Scanner;

//


public class diffrence_of_next_and_nextLine {


    public static void main(String[]args){
//    	
    	Scanner s = new Scanner(System.in);
//    	
//    	입력예시 ㅁㄴㅇ ㅋㅊㅌ
    	String ins = s.next();
    	System.out.print(ins);//next()는 공백 기준으로 입력을 마친다.
    	//출력은ㅁㄴㅇ 
    	
    	System.out.print("/");//그리고 바로 슬레시 ㅁㄴㅇ/
    	String ins2 = s.nextLine();// 공백과 ㅋㅊㅌ가 입력돼서
    	System.out.print(ins2);  // ㅁㄴㅇ/ ㅋㅊㅌ가 출력
    	
    	
    	

    	
    	
    	
    	

    	
    	
    	


      
    }


}
